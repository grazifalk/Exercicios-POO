package br.com.residencia.poo.errinhos;

import java.util.Scanner;

/*O programa “Tempo de vida” irá imprimir a soma das idades de todos os
colegas da sua equipe (6 pessoas). Pergunte a cada um a idade e não
esqueça a sua! Depois faça a atribuição direta da expressão em uma
variável inteira.*/

public class ListaUmExDois {

	public static void main(String[] args) {
		int idade, soma = 0, cont = 1;
		Scanner info = new Scanner(System.in);
		
		while (cont <= 6) {
			System.out.println("Insira a idade " + cont + ": ");
		    idade = info.next();
		    soma += idade;
		    cont++;
		}
		System.out.println("O tempo de vida é: " + soma + " anos!");
		
		info.close();
	}

}
